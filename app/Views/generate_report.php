<script>
    document.getElementById('submitButton').addEventListener('click', function() {
        // Get form values
        const fileName = document.getElementById('excel-filename').value.trim();
        const fileNameError = document.getElementById('filename-error');

        // Validation
        if (!fileName) {
            // Show error if the file name is empty
            fileNameError.style.display = 'block';
        } else {
            // Hide error and submit the form
            fileNameError.style.display = 'none';
            document.getElementById('reportForm').submit();
        }
    });
</script>

